/**
 * patch-gradle-jdk.js
 *
 * Uses an existing local JDK for @react-native/gradle-plugin after every
 * `npm install`. This is required on MDM-managed Macs where Gradle's Java
 * toolchain auto-provisioning is blocked by policy.
 *
 * The React Native Gradle plugin is compiled as an included Gradle build and
 * requests `jvmToolchain(17)`. Without this patch Gradle tries to download
 * JDK 17 from Eclipse Adoptium, which triggers an MDM approval dialog and
 * blocks the build.
 *
 * This script points the included build at an existing local JDK and disables
 * auto-download. It also rewrites plugin toolchain declarations to the local
 * JDK major version when needed.
 */

const fs = require("fs");
const path = require("path");
const os = require("os");

const DEFAULT_JDK = path.join(os.homedir(), "android-studio-jdk");

const JDK_HOME = process.env.GRADLE_JDK_HOME || DEFAULT_JDK;

const TARGET_DIR = path.join(
  __dirname,
  "..",
  "node_modules",
  "@react-native",
  "gradle-plugin",
);

const EXTRA_PATCH_DIR = path.join(
  __dirname,
  "..",
  "node_modules",
  "react-native",
  "ReactAndroid",
);

const TARGET_FILE = path.join(TARGET_DIR, "gradle.properties");

function getJavaMajor(jdkHome) {
  try {
    const releaseFile = path.join(jdkHome, "release");
    const raw = fs.readFileSync(releaseFile, "utf8");
    const match = raw.match(/JAVA_VERSION="(\d+)(?:[.\-_].*)?"/);
    return match ? Number(match[1]) : null;
  } catch {
    return null;
  }
}

function walkFiles(dir, out = []) {
  const entries = fs.readdirSync(dir, { withFileTypes: true });
  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      walkFiles(fullPath, out);
    } else {
      out.push(fullPath);
    }
  }
  return out;
}

const CONTENT = [
  "# Auto-generated by scripts/patch-gradle-jdk.js — do not edit manually.",
  "# Re-generated on every `npm install` via the postinstall hook.",
  "#",
  "# Points the @react-native/gradle-plugin included build at an existing",
  "# local JDK so Gradle never tries to auto-provision a JDK (blocked by MDM).",
  `org.gradle.java.home=${JDK_HOME}`,
  `org.gradle.java.installations.paths=${JDK_HOME}`,
  "org.gradle.java.installations.auto-download=false",
  "",
].join("\n");

if (!fs.existsSync(TARGET_DIR)) {
  console.warn(
    "[patch-gradle-jdk] Skipped: node_modules/@react-native/gradle-plugin not found.",
  );
  process.exit(0);
}

if (!fs.existsSync(JDK_HOME)) {
  console.warn(
    `[patch-gradle-jdk] WARNING: JDK path not found at ${JDK_HOME}.`,
  );
  console.warn(
    "[patch-gradle-jdk] Set GRADLE_JDK_HOME or create the README symlink.",
  );
  process.exit(0);
}

const javaMajor = getJavaMajor(JDK_HOME);
if (!javaMajor) {
  console.warn(
    `[patch-gradle-jdk] WARNING: Could not detect JAVA_VERSION from ${JDK_HOME}.`,
  );
  process.exit(0);
}

fs.writeFileSync(TARGET_FILE, CONTENT, "utf8");
console.log(`[patch-gradle-jdk] Wrote ${TARGET_FILE}`);

const patchDirs = [TARGET_DIR, EXTRA_PATCH_DIR].filter((dir) =>
  fs.existsSync(dir),
);

const patchableFiles = patchDirs.flatMap((dir) =>
  walkFiles(dir).filter(
    (file) =>
      file.endsWith(".gradle.kts") ||
      file.endsWith(".gradle") ||
      file.endsWith(".kt") ||
      file.endsWith(".kts"),
  ),
);

let patchedCount = 0;
for (const file of patchableFiles) {
  const before = fs.readFileSync(file, "utf8");
  const after = before
    .replace(/jvmToolchain\s*\(\s*17\s*\)/g, `jvmToolchain(${javaMajor})`)
    .replace(
      /JavaLanguageVersion\.of\s*\(\s*17\s*\)/g,
      `JavaLanguageVersion.of(${javaMajor})`,
    )
    .replace(/JavaVersion\.VERSION_17/g, `JavaVersion.VERSION_${javaMajor}`)
    .replace(/JvmTarget\.JVM_17/g, `JvmTarget.JVM_${javaMajor}`);

  if (after !== before) {
    fs.writeFileSync(file, after, "utf8");
    patchedCount += 1;
  }
}

console.log(`[patch-gradle-jdk] Using JDK ${javaMajor} at ${JDK_HOME}`);
console.log(
  `[patch-gradle-jdk] Patched toolchain declarations in ${patchedCount} file(s).`,
);
